// you can write strings with single quotes and also in double quotes

let name1 = "Mihir";
console.log(name1);

let name2 = 'Mihir';
console.log(name2);

let n1 = 'mihir is best';
console.log(n1);

let n2 = "mihir is best";
console.log(n2);

// we can't use any quotes within quotes so we have back tic

let n3 = `Some one says that , "Mihir is best"`;
console.log(n3);

let n4 = `${name1} is best in the world`;
console.log(n4);